package com.nibodha.lgaas.dto;

public class AllLight {
	
	private Long id;
	private boolean status;
	private Long brightness;
	private String lotitude;
	private String latitude;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public boolean isStatus() {
		return status;
	}
	public void setStatus(boolean status) {
		this.status = status;
	}
	public Long getBrightness() {
		return brightness;
	}
	public void setBrightness(Long brightness) {
		this.brightness = brightness;
	}
	public String getLotitude() {
		return lotitude;
	}
	public void setLotitude(String lotitude) {
		this.lotitude = lotitude;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	
	

}
